const stakingCalculator = (
  stakingContracts,
  tokenAddresses,
  chain = "ethereum"
) => {
  let tokensStakings = {};
  return async (timestamp, _ethBlock, chainBlocks) => {
    for (let i = 0; i < tokenAddresses.length; i++) {
      let staking = await stakings(stakingContracts, tokenAddresses[i], chain)(
        timestamp,
        _ethBlock,
        chainBlocks
      );

      let stakingIndex = tokenAddresses[i].toLowerCase();
      if (chain === "ethereum") {
        tokensStakings[`${chain}:${tokenAddresses[i]}`] = staking[stakingIndex];
      } else {
        let keys = Object.keys(staking);
        tokensStakings[`${keys[0]}`] = staking[keys[0]];
      }
    }
    return tokensStakings;
  };
};
